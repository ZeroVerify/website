<h1>ZeroVerify</h1>

<h2>Project Overview</h2>

<p>
	ZeroVerify is a secure credential verification system designed to help universities and
	organizations verify academic credentials without relying on centralized third-party databases.
	The system focuses on improving trust, privacy, and verification efficiency through cryptographic
	verification methods.
</p>

<p>
	The goal of ZeroVerify is to allow institutions to issue credentials that can be verified quickly
	and securely by employers or other organizations. Instead of storing sensitive data in centralized
	systems, ZeroVerify enables verification through secure keys and decentralized validation
	techniques.
</p>

<p>
	This project explores the architecture, product design, and technical implementation required to
	support secure credential verification while maintaining user privacy and minimizing the risk of
	data breaches.
</p>
